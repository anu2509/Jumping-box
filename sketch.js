var canvas;
var music;
var purple,pink,red,white;
var box,Egdes;
var b1, b2, b3;

function preload(){
    music = loadSound("music.mp3");
}


function setup(){
    canvas = createCanvas(800,600);

    //create 4 different surfaces
    purple = createSprite(50,590,200,20);
    purple.debug = true;
    


    pink = createSprite(260,590,200,20);
    pink.debug = true;


    red = createSprite(470,590,200,20);
    red.debug = true;

    white = createSprite(680,590,200,20);
    white.debug = true;  


    //create box sprite and give velocity

    box = createSprite(350, 100,50,50);
    box.x = Math.round(random(50,750))
    box.velocityY = 6;
    box.velocityX = 6;
    box.debug = true;

   
   b1 = createSprite(400,0,800,1);
   b2 = createSprite(0,300,1,600);
   b3 = createSprite(800,300,1,600);
   
   b1.visible = false;
   b2.visible = false;
   b3.visible = false;
   
   
   
   

}

function draw() {
    background(0,0,0);
    //create edgeSprite
    box.shapeColor = rgb(255,255,255);
    purple.shapeColor = rgb(153, 0, 115);
    pink.shapeColor = rgb(255, 51, 255);
    red.shapeColor = rgb(255, 77, 77);
    white.shapeColor = rgb(255, 128, 128); 

   box.bounceOff(red);
   box.bounceOff(purple);
   box.bounceOff(pink);
   box.bounceOff(white);
   box.bounceOff(b1);
   box.bounceOff(b2);
   box.bounceOff(b3);

   
    //add condition to check if box touching surface and make it box
    if (box.isTouching(red)){

       box.shapeColor = rgb(255, 77, 77);
       music.play();
       

    }
    
    if (box.isTouching(pink)){

        box.shapeColor = rgb(255, 51, 255);
        music.play();
       

     }

     if (box.isTouching(purple)){

        box.shapeColor = rgb(153, 0, 115);
        music.play();
        

     }

     if (box.isTouching(white)){

        box.shapeColor = rgb(255, 128, 128); 
        music.play();  
        

     }


    
    

    
    

    drawSprites();  
}































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































